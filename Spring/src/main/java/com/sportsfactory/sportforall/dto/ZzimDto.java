package com.sportsfactory.sportforall.dto;

public class ZzimDto {
	private String CATEGORY;
	private int CODE;
	private String TITLE;
	private String WRITE_DATE;
	private String WRITE_TIME;
	private String DATETIME;
	
	private int USER_ID;
	private int CLUB_CODE;
	
	
	public int getCLUB_CODE() {
		return CLUB_CODE;
	}
	public void setCLUB_CODE(int cLUB_CODE) {
		CLUB_CODE = cLUB_CODE;
	}
	public int getUSER_ID() {
		return USER_ID;
	}
	public void setUSER_ID(int uSER_ID) {
		USER_ID = uSER_ID;
	}
	public String getDATETIME() {
		if(WRITE_DATE!=null && WRITE_TIME!=null)
			return WRITE_DATE + " " + WRITE_TIME;
		else
			return DATETIME;
	}
	public int getCODE() {
		return CODE;
	}
	public void setCODE(int cODE) {
		CODE = cODE;
	}
	public String getTITLE() {
		return TITLE;
	}
	public void setTITLE(String tITLE) {
		TITLE = tITLE;
	}
	public void setDATETIME(String dATETIME) {
		DATETIME = dATETIME;
	}
	public String getWRITE_DATE() {
		return WRITE_DATE;
	}
	public void setWRITE_DATE(String wRITE_DATE) {
		WRITE_DATE = wRITE_DATE;
	}
	public String getWRITE_TIME() {
		return WRITE_TIME;
	}
	public void setWRITE_TIME(String wRITE_TIME) {
		WRITE_TIME = wRITE_TIME;
	}
	public String getCATEGORY() {
		return CATEGORY;
	}
	public void setCATEGORY(String cATEGORY) {
		CATEGORY = cATEGORY;
	}
}
